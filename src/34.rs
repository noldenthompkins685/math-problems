// Example Rust code demonstrating a simple loop and conditional statements

fn main() {
    let number = 5;

    // Loop from 1 to 10
    for i in 1..=10 {
        println!("{}", i);
    }

    if number > 3 {
        println!("Number is greater than 3");
    } else {
        println!("Number is not greater than 3");
    }
}
